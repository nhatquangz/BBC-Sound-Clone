import Foundation
import UIKit


class ContainerViewCell: UICollectionViewCell {
	override init(frame: CGRect) {
		super.init(frame: frame)
		setup()
	}
	
	required init?(coder aDecoder: NSCoder) {
		super.init(coder: aDecoder)
		setup()
	}
	
	private func setup() {
		backgroundColor = .random
	}
}
